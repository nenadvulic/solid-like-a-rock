#!/usr/bin/env bash
#
# Benchmark solid-like-a-rock against a real open-source project (isowords),
# SwiftLint-style: clone at a pinned SHA, generate a config with `init --freeze`,
# then time `lint` (median of 3 runs). Read-only with respect to upstream.
#
# Usage: scripts/benchmark.sh [binary-path]
#   binary-path  defaults to .build/release/solid-like-a-rock (built if missing)

set -euo pipefail
export LC_ALL=C   # consistent decimal points in /usr/bin/time output

ISOWORDS_REPO="https://github.com/pointfreeco/isowords.git"
ISOWORDS_SHA="c727d3a7c49cf0c98f2fa4f24c562f81e30165f7"
CACHE_DIR="/tmp/slr-benchmark/isowords"
RUNS=3

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
BIN="${1:-$ROOT/.build/release/solid-like-a-rock}"

# --- Binary -----------------------------------------------------------------
if [[ ! -x "$BIN" ]]; then
  echo "→ building release binary…"
  swift build -c release --package-path "$ROOT"
fi

# --- Checkout (cached, pinned) ----------------------------------------------
# isowords uses Git LFS for image assets; skip the smudge — we only lint .swift.
export GIT_LFS_SKIP_SMUDGE=1
if [[ ! -d "$CACHE_DIR/.git" ]] || ! git -C "$CACHE_DIR" cat-file -e "$ISOWORDS_SHA" 2>/dev/null; then
  rm -rf "$CACHE_DIR"; mkdir -p "$CACHE_DIR"
  echo "→ fetching isowords @ ${ISOWORDS_SHA:0:12}…"
  git -C "$CACHE_DIR" init -q
  git -C "$CACHE_DIR" remote add origin "$ISOWORDS_REPO"
  if ! git -C "$CACHE_DIR" fetch -q --depth 1 origin "$ISOWORDS_SHA"; then
    echo "error: could not fetch isowords — check your network." >&2; exit 1
  fi
fi
git -C "$CACHE_DIR" checkout -q "$ISOWORDS_SHA"

# --- Timing helper (wall clock, via /usr/bin/time -p) -------------------------
real_time() { # real_time <cmd...> -> seconds on stdout, cmd output discarded
  { /usr/bin/time -p "$@" >/dev/null 2>/tmp/slr-bench-out; } 2>/dev/null || true
  awk '/^real/ {print $2}' /tmp/slr-bench-out
}

# --- init --freeze ------------------------------------------------------------
rm -f "$CACHE_DIR/.solid.yml"
INIT_TIME="$(real_time "$BIN" init --force --freeze "$CACHE_DIR")"
MODULES="$(grep -c '^  - name:' "$CACHE_DIR/.solid.yml" || true)"

# --- lint × N, median ----------------------------------------------------------
LINT_OUT="$("$BIN" lint --config "$CACHE_DIR/.solid.yml" "$CACHE_DIR" 2>&1 | tail -1)"
FILES="$(echo "$LINT_OUT" | grep -oE '[0-9]+ file' | grep -oE '[0-9]+' || echo '?')"

TIMES=()
for i in $(seq "$RUNS"); do
  T="$(real_time "$BIN" lint --config "$CACHE_DIR/.solid.yml" "$CACHE_DIR")"
  TIMES+=("$T")
  echo "  lint run $i: ${T}s"
done
MEDIAN="$(printf '%s\n' "${TIMES[@]}" | sort -n | awk 'NR==2')"

# --- Report -------------------------------------------------------------------
echo
echo "── solid-like-a-rock benchmark ─────────────────────────"
echo "machine:       $(sysctl -n machdep.cpu.brand_string)"
echo "project:       isowords @ ${ISOWORDS_SHA:0:12}"
echo "swift files:   $FILES"
echo "modules:       $MODULES (layers generated by init --freeze)"
echo "init --freeze: ${INIT_TIME}s"
echo "lint (median of $RUNS): ${MEDIAN}s"
echo "last lint output: $LINT_OUT"
